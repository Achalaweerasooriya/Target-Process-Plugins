using System;

namespace Tp.Integration.Common
{
	/// <summary>
	/// Data Transfer object of Attachment. Represents file attached to entity.
	/// </summary>
	[Serializable]
	public partial class AttachmentDTO : DataTransferObject
	{
		/// <summary>
		/// Gets or sets the ID.
		/// </summary>
		/// <value>The ID.</value>		
		[PrimaryKey]
		public override int? ID
		{
			get { return AttachmentID; }
			set
			{
				if (value == int.MinValue)
					value = null;

				AttachmentID = value;
			}
		}

		/// <summary>
		/// Gets or sets the Attachment ID.
		/// </summary>
		/// <value>The Attachment ID.</value>
		[PrimaryKey]
		public int? AttachmentID { get; set; }


		/// <summary>
		/// Gets or sets the Original File Name. Original name of the file
		/// </summary>
		/// <value>The Original File Name.</value>
		public String OriginalFileName { get; set; }

		/// <summary>
		/// Gets or sets the Unique File Name. Unique name of the file that generated by TargetProcess
		/// </summary>
		/// <value>The Unique File Name.</value>
		public String UniqueFileName { get; set; }

		/// <summary>
		/// Gets or sets the Create Date. Date when attachment has been created
		/// </summary>
		/// <value>The Create Date.</value>
		public DateTime? CreateDate { get; set; }

		/// <summary>
		/// Gets or sets the Description. Attachment description
		/// </summary>
		/// <value>The Description.</value>
		public String Description { get; set; }

		/// <summary>
		/// Gets or sets the General ID. Reference to entity. Attachment can be added to almost any entity
		/// </summary>
		/// <value>The General ID.</value>
		[ForeignKey]
		public Int32? GeneralID { get; set; }

		/// <summary>
		/// Gets or sets the Message ID. Reference to entity. Attachment can be added to message
		/// </summary>
		/// <value>The Message ID.</value>
		[ForeignKey]
		public Int32? MessageID { get; set; }

		/// <summary>
		/// Gets or sets the Owner ID. Person who added the attachment
		/// </summary>
		/// <value>The Owner ID.</value>
		[ForeignKey]
		public Int32? OwnerID { get; set; }

		/// <summary>
		/// Gets or sets the Attachment File ID. Attachment may be stored in database or on hard drive. AttachmentFile contains actual attachment content
		/// </summary>
		/// <value>The Attachment File ID.</value>
		[ForeignKey]
		public Int32? AttachmentFileID { get; set; }


		/// <summary>
		/// Gets or sets the General Name. Reference to entity. Attachment can be added to almost any entity
		/// </summary>
		/// <value>The General Name.</value>
		[RelationName]
		public virtual string GeneralName { get; set; }
	}


	/// <summary>
	/// Attachment fields
	/// </summary>
	public enum AttachmentField
	{
		/// <summary>
		/// Original File Name
		/// </summary>		
		OriginalFileName,
		/// <summary>
		/// Unique File Name
		/// </summary>		
		UniqueFileName,
		/// <summary>
		/// Create Date
		/// </summary>		
		CreateDate,
		/// <summary>
		/// Description
		/// </summary>		
		Description,
		/// <summary>
		/// General ID
		/// </summary>		
		GeneralID,
		/// <summary>
		/// Message ID
		/// </summary>		
		MessageID,
		/// <summary>
		/// Owner ID
		/// </summary>		
		OwnerID,
		/// <summary>
		/// Attachment File ID
		/// </summary>		
		AttachmentFileID,
		/// <summary>
		/// General Name
		/// </summary>		
		GeneralName,
	}
}